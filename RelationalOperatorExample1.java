class RelationalOperatorExample1{
public static void main(String[] args){
int i=100;
int j=200;
boolean result=i<j;
System.out.println("i<j:"+result);
boolean result1=j<i;
System.out.println("j<i:"+result1);
boolean result2=i>j;
System.out.println("i>j:"+result2);
boolean result3=j>i;
System.out.println("j>i:"+result3);

}
}







